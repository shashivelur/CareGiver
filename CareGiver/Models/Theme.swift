import UIKit


enum Theme {
    static let accent = UIColor.systemIndigo
    static let cardBackground = UIColor.secondarySystemBackground
    static let selectedTabBackground = UIColor.systemIndigo
    static let unselectedTabBackground = UIColor.tertiarySystemFill
    static let title = UIColor.label
    static let subtitle = UIColor.secondaryLabel
    static let title2 = UIColor.white
    static let subtitle2 = UIColor.lightGray

    // Unified color tokens used across the app
    static let background = UIColor.white
    static let secondaryBackground = UIColor.secondarySystemBackground

    static let textPrimary = UIColor.label
    static let textSecondary = UIColor.secondaryLabel

    // Foreground color to use on top of accent backgrounds
    static let onAccent = UIColor.white
}
